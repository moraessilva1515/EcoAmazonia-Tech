export function getTranslation(lang: Language, key: string, ...args: any[]): string {
    const keys = key.split('.');
    let result = data[lang] as any;
    for (const k of keys) {
        result = result?.[k];
        if (result === undefined) {
            let fallbackResult = data.pt as any;
            for (const fk of keys) {
                fallbackResult = fallbackResult?.[fk];
                if (fallbackResult === undefined) return key; 
            }
            result = fallbackResult;
            break;
        }
    }
    
    if (typeof result === 'string' && args.length > 0) {
        return result.replace(/{(\d+)}/g, (match, number) => {
            return typeof args[number] !== 'undefined' ? args[number] : match;
        });
    }
    return result || key;
}